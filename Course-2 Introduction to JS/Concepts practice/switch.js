//checking the type of medal they get based on the place using  switch condition
var place = 'first'
switch(place)
{
    case 'first' : console.log("Gold medal")
    break;
    case 'second' : console.log("Silver medal")
    break;
    case 'third' : console.log("Bronze medal")
    break;
    default:console.log('No medal')
    break;
}